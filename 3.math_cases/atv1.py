dia = input("Digite dias da semana")

match dia:
    case "segunda":   
        print ("Hoje é segunda-feira.")
    case "terca":  
        print ("Hoje é terca-feira.")
    case "quarta":
        print ("Hoje é quarta-feira.")
    case "quinta": 
        print ("Hoje é quinta-feira.")
    case "sexta": 
        print ("Hoje é sexta-feira.")
    case "final de semana":
        print ("Hoje é final de semana.")
       
      
        
       
        